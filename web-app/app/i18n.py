def compile_translations(translations_dir: str) -> None:
    """Write .mo files for each locale in ``TRANSLATIONS`` into ``translations_dir``.

    Called once on app startup; produces files Flask-Babel reads at request
    time. Safe to call repeatedly — it overwrites existing .mo files.
    """
    for locale, mapping in TRANSLATIONS.items():
        catalog = Catalog(locale=locale, domain="messages")
        # Empty header msgid is required by gettext for the metadata block.
        catalog.add("", "")
        for msg_id, translated in mapping.items():
            if translated:
                catalog.add(msg_id, translated)

        out_dir = os.path.join(translations_dir, locale, "LC_MESSAGES")
        os.makedirs(out_dir, exist_ok=True)
        out_path = os.path.join(out_dir, "messages.mo")
        with open(out_path, "wb") as f:
            write_mo(f, catalog)


def client_strings_for(locale: str) -> dict:
    """Return ``{english_key: translated}`` for the keys exposed to client JS."""
    out = {}
    mapping = TRANSLATIONS.get(locale, {})
    for key in CLIENT_STRING_KEYS:
        out[key] = mapping.get(key, key)
    return out
